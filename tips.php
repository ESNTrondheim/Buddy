                <p>
                    The purpose of the buddy program is to assist international students in Trondheim with integration in the Norwegian student life. The buddy on his side, get the opportunity to practice a foreign language, get acquainted with different cultures and get involved in the international community in Trondheim.
                    The buddy is a (Norwegian) student in Trondheim, who has signed up to provide a friendly support to a foreign student arriving to Trondheim. We will do our best to find a matching buddy and exchange student. Since many of the Norwegian students are interested in practicing a foreign language, we are trying to match according to the language the foreigner speaks. We will also try to match due to gender and campus. The amount of interaction and support is completely dependent on the interests of both of you. However, to avoid possible misunderstandings, we have some simple rules:
                </p>
                <ul>
                    <li> Both buddy and guest agree upon and exchange an efficient contact method -
                        telephone, e-mail, etc.</li>
                    <li> A buddy should be able, on request of the guest, to give support for at
                        least 15 minutes per week (based on a mutual agreement of suitable time) in form
                        of a common meeting </li>
                    <li> During the whole semester, buddy takes the guest to at least one social
                        event (party, visit of some culture, trip, etc.), where the majority of the
                        students will be Norwegian students </li>
                    <li> A buddy has the advantage of participation at social program organized
                        (and or sponsored) by the ISU, ESN, and the International Office. </li>
                </ul>
                <p>
                    These rules are just to give you a little hint, and are not strictly obeyed. On the start of every semester ESN arranges a Buddy welcome party for the participants to the program. We will also arrange several other buddy-parties and activities during the semester. 

                    We hope to see you! 

                    ESN Trondheim
